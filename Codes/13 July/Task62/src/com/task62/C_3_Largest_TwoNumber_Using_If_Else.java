package com.task62;

public class C_3_Largest_TwoNumber_Using_If_Else {
	public static void main(String[] args) {

	    int a = 20;
	    int b = 35;

	    if (a > b) {
	        System.out.println(a + " is the largest number");
	    } else {
	        System.out.println(b + " is the largest number");
	    }
	}
}
