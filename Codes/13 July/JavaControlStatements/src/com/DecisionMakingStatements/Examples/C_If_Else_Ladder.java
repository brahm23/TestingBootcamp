package com.DecisionMakingStatements.Examples;

public class C_If_Else_Ladder {
	public static void main(String[] args) {
		
		String city = "Meerut69";
		
		if(city == "Meerut") {
			System.out.println("Meerut");
		} else if (city == "Agra") {
			System.out.println("Agra");
		} else {
			System.out.println(city);
		}
		
		
		}

}
