package com.Task53_Operators.Examples;

public class G_Ternary_Operator {
	public static void main(String[] args) {

	    // Project Example: Movie Ticket System
	    // Operators Used: Ternary Operator (? and :) and Relational Operator (>=)

	    int age = 15;

	    String ticket = (age >= 18) ? "Adult Ticket" : "Child Ticket";

	    System.out.println(ticket);
	}
}
